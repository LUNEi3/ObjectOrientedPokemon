    @Override
    public void addNotify() {
        super.addNotify();
        // This method is called automatically when the panel is 
        // added to the JFrame and becomes displayable.
        if (gameThread == null) {
            startGameThread();
        }
        
        this.requestFocusInWindow();
    }
    
    @Override
    public void run() {
        double drawInterval = 1000000000 / 60; // 60 FPS
        double delta = 0;
        long lastTime = System.nanoTime();
        long currentTime;
        
        while (gameThread != null) { 

            currentTime = System.nanoTime();
            delta += (currentTime - lastTime) / drawInterval;
            lastTime = currentTime;

            if (delta >= 1) {
                if (!isStopped) {
                    update();
                    repaint();
                }
                delta--;
            }

            if (isStopped) {
                 break; 
            }
        }
    }

    private void startGameThread() {
//        System.out.println("startGameThread() is running...");
        gameThread = new Thread(this);
        gameThread.start();
    }
    
    private void update() {
//        System.out.println("Update() is running...");
        if (gameStateOn) {
            player.update();
        } else {
            //
        }
    }
    
